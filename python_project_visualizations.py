import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns
import plotly.express as px

#load data and inspection
data=pd.read_csv('/content/country_wise_latest.csv')

#Initial Observations
data.head()
data.info()
data.describe()

#Data Cleaning
data.dropna(inplace=True)

#Data with WHO region aggregation
data_region = data.groupby('WHO Region').agg({
    'Confirmed': 'sum',
    'Deaths': 'sum',
    'Recovered': 'sum',
    'Active': 'sum',
    'New cases': 'sum',
    'New deaths': 'sum',
    'New recovered': 'sum'
}).reset_index()

# Calculate rates after aggregation
data_region['Case_Fatality_Rate'] = (data_region['Deaths'] / data_region['Confirmed'] * 100).round(2)
data_region['Recovery_Rate'] = (data_region['Recovered'] / data_region['Confirmed'] * 100).round(2)
data_region.head()

#Save to csv
data_region.to_csv('data_region.csv', index=False)

# Data without WHO aggregation

#Case Fatality Rate (CFR)
data['Case_Fatality_Rate']=(data['Deaths']/data['Confirmed']*100).round(2)
data.head()

#Recovery Rate
data['Recovery_Rate']=(data['Recovered']/data['Confirmed']*100).round(2)
data.head()


#Selecting Relevant columns
data=data[['Country/Region','Confirmed','Deaths','Recovered','Active','Case_Fatality_Rate','Recovery_Rate','New deaths','New cases','New recovered','WHO Region']]
data.head()

#save to csv
data.to_csv('data_without_region_agg.csv',index=False)

