"""# Part 3: Interactive Visualizations with Plotly"""

import plotly.express as px

#Interactive Line Plot
fig=px.line(top_deaths,y='Deaths',x='Country/Region',title='Top 5 Countries by Deaths',markers=True,
            hover_data={
                'Case_Fatality_Rate': ':.2f',
                'Confirmed': True
            })
fig.update_layout(xaxis_title='Country',yaxis_title='Deaths',xaxis_tickangle=-45)
fig.show()

#Interactive Facet Plot
fig = px.scatter(data, x='New cases', y='New deaths',
                 facet_col='WHO Region', facet_col_wrap=3,
                 color='WHO Region',
                 hover_name='Country/Region',
                 title='New Cases vs New Deaths by Region')
fig.update_traces(marker=dict(size=10, opacity=0.6))
fig.for_each_annotation(lambda a: a.update(text=a.text.split("=")[-1]))  # Clean titles
fig.update_layout(showlegend=True, font=dict(size=12))

fig.show()